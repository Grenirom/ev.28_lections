# Репозиторий - это хранилище вашего кода и истории его изменнений

#GitHub - это вебсайт для размещения git-репозиториев в совместной разработке проектов


# Команды git:

# 1. git init - создает новый локальный репозиторий, в той директории в которой была прописана команда
# (Команда вводится 1 раз в начале)

#2. git add - команда для добавления изменений в систему отслеживания гита


#3. git commit -m '<comment>' - Это команда для сохранения всех изменений в локальном репозитории
# с комментарием к ним


#4. git remote add <origin> <URL> -> эта команда создает связь между локальным репозиторием 
# и удаленным в гитхабе

#5. git push <origin> <main> - команда отправки всех ваших изменений на удаленный репо

